package aula01;

public class Exemplo01 {
	public static void main(String[] args) {
		int x =10;
		System.out.println(x++);//10(11)[adiciona um depois de print ]
		System.out.println(++x);//12 [adiciona um antes de print ]
		System.out.println(x--);//12(11)[remove um depois de print ]
		System.out.println(--x);//10[remove um antes de print ]
	}
}